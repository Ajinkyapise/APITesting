package com.sep.models.requests;

public class RequestPojo {
	private String owner;
	private String repo;

	// Getters and setters
	public String getOwner() {
		return owner;
	}

	public void setOwner(String owner) {
		this.owner = owner;
	}

	public String getRepo() {
		return repo;
	}

	public void setRepo(String repo) {
		this.repo = repo;
	}

}
